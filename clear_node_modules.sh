#!/bin/bash
PackagesPath="$(pwd)/packages"  # 使用 $(pwd) 获取当前目录
PackagesList=$(ls "$PackagesPath")  # 使用 $(ls ...) 执行命令并捕获输出
NodeModules="node_modules"
Dist="dist"
echo "Packages 路径：$PackagesPath"
echo "包列表："
echo "$PackagesList"
for path in $PackagesList;
do
  NodeModulesFilePath="$PackagesPath/$path/$NodeModules"
  DistFilePath="$PackagesPath/$path/$Dist"
  echo "删除文件夹 $NodeModulesFilePath"
  rm -rf $NodeModulesFilePath
  echo "删除文件夹 $DistFilePath"
  rm -rf $DistFilePath
done;